CREATE TABLE LOCATION
(
    LOCATION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CITY VARCHAR2(50) NOT NULL,
    STATE VARCHAR2(2) NOT NULL
);

CREATE TABLE POLICE_DEPARTMENT
(
    PD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PD_NAME VARCHAR2(100) NOT NULL,
    PD_CITY VARCHAR2(100) NOT NULL,
    PD_STATE VARCHAR2(50) NOT NULL
);

CREATE TABLE CRIME_TYPE
(
    CRIME_TYPE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CRIME_NAME VARCHAR2(50) NOT NULL
);

CREATE TABLE PERPETRATOR
(
    PERP_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PERP_FNAME VARCHAR2(50) NOT NULL,
    PERP_LNAME VARCHAR2(50) NOT NULL,
    PERP_MNAME VARCHAR2(50),
    PERP_DOB DATE NOT NULL
);

CREATE TABLE INCIDENT
(
    INCIDENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    INCIDENT_DATE DATE NOT NULL,
    INCIDENT_SEVERITY VARCHAR2(10) NOT NULL,
    LOCATION_ID NUMBER NOT NULL,
    CRIME_TYPE_ID NUMBER NOT NULL,
    PD_ID NUMBER NOT NULL,

    FOREIGN KEY (LOCATION_ID) REFERENCES LOCATION(LOCATION_ID),
    FOREIGN KEY (CRIME_TYPE_ID) REFERENCES CRIME_TYPE(CRIME_TYPE_ID),
    FOREIGN KEY (PD_ID) REFERENCES POLICE_DEPARTMENT(PD_ID)
);

CREATE TABLE INCIDENT_PERP
(
    INCIDENT_ID NUMBER NOT NULL,
    PERP_ID NUMBER NOT NULL,

    PRIMARY KEY (INCIDENT_ID, PERP_ID),

    FOREIGN KEY (INCIDENT_ID) REFERENCES INCIDENT(INCIDENT_ID),
    FOREIGN KEY (PERP_ID) REFERENCES PERPETRATOR(PERP_ID)
);

CREATE TABLE INCIDENT_REPORT
(
    INCIDENT_ID NUMBER PRIMARY KEY,
    FILE_DATE DATE NOT NULL,

    FOREIGN KEY (INCIDENT_ID) REFERENCES INCIDENT(INCIDENT_ID)
);

CREATE TABLE INCIDENT_UPDATE
(
    INCIDENT_ID NUMBER NOT NULL,
    UPDATE_NUM NUMBER NOT NULL,
    UPDATE_DATE DATE NOT NULL,

    PRIMARY KEY (INCIDENT_ID, UPDATE_NUM),
    FOREIGN KEY (INCIDENT_ID) REFERENCES INCIDENT(INCIDENT_ID)
);
